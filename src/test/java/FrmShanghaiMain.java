    private void jMenuItem_TestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem_TestActionPerformed
        // TODO add your handling code here:
        this.DrawingPanelShanghai1.clearObjects();
        File directory = new File(".");
        String fn = null;
        try {
            fn = directory.getCanonicalPath();
        } catch (IOException ex) {
            Logger.getLogger(FrmShanghaiMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        fn = this.getClass().getResource("/sample/test2.grd").getPath();
        //fn = fn + File.separator + "sample" + File.separator + "test2.grd";
        File aFile = new File(fn);
        if (aFile.isFile()) {
            try {
                double[][] gridData = this.DrawingPanelShanghai1.readSuferGridData(aFile);
                this.DrawingPanelShanghai1.setGridData(gridData);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(FrmShanghaiMain.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(FrmShanghaiMain.class.getName()).log(Level.SEVERE, null, ex);
            }

            double[] values = new double[]{20, 30, 40, 50, 60, 70, 80, 90};
            //double[] values = new double[]{70};
            this.DrawingPanelShanghai1.setContourValues(values);
            this.DrawingPanelShanghai1.tracingContourLines();
            this.DrawingPanelShanghai1.smoothLines();
            this.DrawingPanelShanghai1.getEcllipseClipping();
            this.DrawingPanelShanghai1.clipLines();
            this.DrawingPanelShanghai1.tracingPolygons();
            this.DrawingPanelShanghai1.clipPolygons();

            this.DrawingPanelShanghai1.setCoordinate();
            this.DrawingPanelShanghai1.createLegend();
            this.repaint();
        }
    }//GEN-LAST:event_jMenuItem_TestActionPerformed

